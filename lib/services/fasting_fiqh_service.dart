  List<FastingFiqhQuestion> getAllQuestions() {
    return questions;
  }

  List<FastingFiqhQuestion> searchQuestions(String query) {
    if (query.isEmpty) return questions;
    
    final lowercaseQuery = query.toLowerCase();
    return questions.where((question) {
      return question.question.toLowerCase().contains(lowercaseQuery) ||
             question.answer.toLowerCase().contains(lowercaseQuery) ||
             question.category.toLowerCase().contains(lowercaseQuery) ||
             question.keywords.any((keyword) => keyword.toLowerCase().contains(lowercaseQuery));
    }).toList();
  }

  List<String> getCategories() {
    return questions.map((q) => q.category).toSet().toList();
  }

  List<FastingFiqhQuestion> getQuestionsByCategory(String category) {
    return questions.where((q) => q.category == category).toList();
  }
